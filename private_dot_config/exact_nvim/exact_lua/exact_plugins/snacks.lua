return {
  "folke/snacks.nvim",
  priority = 1000,
  lazy = false,
  opts = function()
    math.randomseed(os.time())
    local header = table.concat(headers[math.random(#headers)], "\n")

    return {
      notifier = { enabled = true },
      words = { enabled = true },
      indent = { enabled = true },
      dashboard = {
        preset = {
          header = header,
          keys = {
            { icon = " ", key = "e", desc = "Explorer", action = ":NvimTreeToggle" },
            { icon = " ", key = "f", desc = "Find file", action = ":FzfLua files" },
            { icon = " ", key = "o", desc = "Recent files", action = ":FzfLua oldfiles" },
            { icon = " ", key = "w", desc = "Find words", action = ":FzfLua live_grep" },
            { icon = "󰒲 ", key = "l", desc = "Lazy plugins", action = ":Lazy" },
            { icon = " ", key = "s", desc = "Load last session", action = ":AutoSession restore" },
            { icon = " ", key = "q", desc = "Quit", action = ":qa" },
          },
        },
        sections = {
          { section = "header" },
          { section = "keys", gap = 1, padding = 1 },
          {
            align = "center",
            padding = 1,
            text = function()
              local stats = require("lazy").stats()
              local ms = math.floor(stats.startuptime * 100 + 0.5) / 100
              return { { "⚡ " .. ms .. "ms", hl = "footer" } }
            end,
          },
        },
      },
    }
  end,
}
